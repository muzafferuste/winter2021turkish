package day03_scannerIncrementDecrement;

public class C6_IncrementDecrement {

	public static void main(String[] args) {
		
		// bir variable'in degerini toplayarak arttirmak icin 3 yontem kullanabilirim
		
		int number = 10;
		
		// 1. yontem (acemi yontemi)
		number = number +5;
		
		// 2. yontem (usta isi)
		number += 8;
		
		// 3. yontem sadece 1 artirmak icin
		number++;
		
		// bu yontemler her operator icin kullanilabilir
		// 3. yontem sadece toplama ve cikarma icin gecerlidir
		
		// number variable'ini 6 eksiltin
		number -= 6;
		
		// number variable'ini 2'ye bolun
		number /= 2;
		
		// nuber variable'ini 3 ile carpin
		number *= 3;
		
		// number variable'ini 1 eksiltin
		number--;
		
		
		
	}
	
}
