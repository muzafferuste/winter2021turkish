package day03_scannerIncrementDecrement;

public class C7_PreIncrementPostIncrement {

	public static void main(String[] args) {
		
		int num = 20;
		
		num++; // num=21
		
		System.out.println(num); // 21
		
		System.out.println(num++); // 21 once yazdiriyor sonra arttiriyor
		
		System.out.println(num); // 22
		
		System.out.println(++num); // once arttirir 23 yapar sonra yazdirir
		
		System.out.println(num); // 23
		
	}

}
